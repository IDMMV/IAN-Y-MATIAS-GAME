function container_eduRender(subject, isMath, data, icon) {
  const container = document.getElementById('modalBody');
  let level = 1, idx = 0, score = 0, questions = [], answered = false;
  const maxLevel = isMath ? (subject==='tablas'?8:3) : data.levels.length;

  container.innerHTML = `
    <div class="game-container" style="background:#0a0a14;padding:16px;min-height:300px">
      <div style="display:flex;justify-content:space-between;align-items:center;color:#fff;font-weight:800;margin-bottom:14px">
        <span>${icon} Nivel <span id="eduLevel">1</span></span>
        <span>✅ <span id="eduScore">0</span></span>
        <span>📋 <span id="eduNum">1</span>/5</span>
      </div>
      <div id="eduQ" style="color:#fff;font-family:'Fredoka One',cursive;font-size:1.5rem;text-align:center;margin:14px 0 6px;min-height:50px;display:flex;align-items:center;justify-content:center"></div>
      <div style="text-align:center;margin-bottom:12px">
        <button id="eduReadBtn" style="background:#FFD600;border:none;color:#2D1B4E;padding:10px 22px;border-radius:20px;font-family:Nunito,sans-serif;font-weight:800;font-size:0.95rem;cursor:pointer;-webkit-tap-highlight-color:transparent">🔊 Leer pregunta</button>
      </div>
      <div id="eduOpts" style="display:grid;grid-template-columns:1fr 1fr;gap:10px"></div>
    </div>
    <div class="game-instructions"><strong>${isMath?'Matemáticas':'Pregunta'}:</strong> Pulsa 🔊 para escuchar. Responde 5 preguntas para pasar de nivel. ¡Cada nivel es más difícil!</div>`;

  // Lee la pregunta y las 4 opciones en voz alta
  function readQuestion() {
    const q = questions[idx]; if (!q) return;
    let txt = q.q.replace('=', 'igual a').replace('×','por').replace('−','menos').replace('+','más') + '. ';
    txt += 'Opciones: ';
    q.o.forEach((o, i) => { txt += `${i+1}. ${o}. `; });
    speak(txt);
  }

  function loadLevel() {
    if (isMath) {
      questions = genMathQuestions(subject, level);
    } else {
      // Tomar las preguntas del nivel, barajar su ORDEN, y barajar las OPCIONES de cada una
      const raw = data.levels[Math.min(level-1, data.levels.length-1)];
      questions = shuffle(raw).map(item => {
        const correctText = item.o[item.a];          // texto de la respuesta correcta
        const shuffledOpts = shuffle(item.o);         // mezclar las 4 opciones de verdad
        return { q: item.q, o: shuffledOpts, a: shuffledOpts.indexOf(correctText) };
      });
    }
    idx = 0; setText('eduLevel', level); render();
  }
  function render() {
    answered = false;
    setText('eduNum', idx+1); setText('eduScore', score); setText('eduLevel', level);
    const q = questions[idx];
    const qEl = document.getElementById('eduQ'); if(!qEl) return;
    qEl.textContent = q.q;
    const opts = document.getElementById('eduOpts'); opts.innerHTML = '';
    q.o.forEach((opt, i) => {
      const btn = document.createElement('button');
      btn.textContent = opt;
      btn.style.cssText = 'background:#2563EB;border:none;color:#fff;padding:18px 10px;border-radius:14px;font-family:Nunito,sans-serif;font-weight:800;font-size:1.1rem;cursor:pointer;-webkit-tap-highlight-color:transparent;transition:transform 0.1s';
      const pick = e => { e.preventDefault(); e.stopPropagation(); answer(i, btn); };
      btn.addEventListener('touchstart', pick, {passive:false});
      btn.addEventListener('click', pick);
      opts.appendChild(btn);
    });
    // conectar botón de leer
    const readBtn = document.getElementById('eduReadBtn');
    if (readBtn) {
      const fire = e => { e.preventDefault(); e.stopPropagation(); readQuestion(); };
      readBtn.ontouchstart = fire;
      readBtn.onclick = fire;
    }
  }
  function answer(i, btn) {
    if (answered) return; answered = true;
    const q = questions[idx];
    const allBtns = document.querySelectorAll('#eduOpts button');
    if (i === q.a) {
      score++; setText('eduScore', score); SFX.point();
      btn.style.background = '#16A34A';
    } else {
      SFX.hit(); btn.style.background = '#DC2626';
      if (allBtns[q.a]) allBtns[q.a].style.background = '#16A34A';
    }
    registerInterval(setTimeout(() => {
      idx++;
      if (idx >= questions.length) {
        // fin del nivel
        if (level >= maxLevel) {
          celebrate();
          awardBadge('first_win');
          if (subject === 'sumas') awardBadge('math_star');
          else if (subject === 'tablas') awardBadge('times_star');
          else if (subject === 'regiones' || subject === 'patrios') awardBadge('peru_expert');
          const c = document.getElementById('eduQ');
          if (c) { document.getElementById('eduOpts').innerHTML=''; c.innerHTML = `🏆 ¡Completaste todo!<br>Aciertos: ${score}`; }
          showToast(`🎉 ¡Terminaste! ${score} aciertos`);
        } else {
          level++;
          showLevelScreen(level, loadLevel, isMath ? '¡Números más grandes!' : '¡Preguntas más difíciles!');
        }
      } else render();
    }, 700));
  }
  loadLevel();
}

// Forma la Palabra (ordenar letras) con niveles
function buildPalabra(container) {
  const WORDS = [
    ['GATO','PERRO','CASA','SOL','PAN'],
    ['ESCUELA','AMIGO','FAMILIA','LIBRO','MESA'],
    ['COMPUTADORA','BICICLETA','MARIPOSA','ELEFANTE','MONTAÑA'],
  ];
  let level=1, score=0, current='', scrambled=[], built=[];
  container.innerHTML = `
    <div class="game-container" style="background:#0a0a14;padding:16px;min-height:300px">
      <div style="display:flex;justify-content:space-between;align-items:center;color:#fff;font-weight:800;margin-bottom:10px">
        <span>🔤 Nivel <span id="paLevel">1</span></span>
        <span>✅ <span id="paScore">0</span></span>
        <button onclick="window.paSkip()" style="background:#7C3AED;border:none;color:white;padding:6px 12px;border-radius:12px;cursor:pointer;font-family:Nunito,sans-serif;font-weight:700">Saltar</button>
      </div>
      <div style="text-align:center;color:#aaa;margin-bottom:6px">Ordena las letras para formar la palabra</div>
      <div style="text-align:center;margin-bottom:10px">
        <button id="paReadBtn" style="background:#FFD600;border:none;color:#2D1B4E;padding:9px 20px;border-radius:18px;font-family:Nunito,sans-serif;font-weight:800;font-size:0.9rem;cursor:pointer;-webkit-tap-highlight-color:transparent">🔊 Escuchar palabra</button>
      </div>
      <div id="paBuilt" style="min-height:54px;display:flex;gap:6px;justify-content:center;flex-wrap:wrap;margin-bottom:14px;border-bottom:2px dashed rgba(255,255,255,0.2);padding-bottom:10px"></div>
      <div id="paLetters" style="display:flex;gap:6px;justify-content:center;flex-wrap:wrap"></div>
    </div>
    <div class="game-instructions"><strong>Cómo jugar:</strong> Pulsa 🔊 para escuchar la palabra. Toca las letras en orden para formarla. ¡Cada nivel son más largas!</div>`;

  function pickWord() {
    const list = WORDS[Math.min(level-1, WORDS.length-1)];
    current = list[Math.floor(Math.random()*list.length)];
    scrambled = current.split('').sort(()=>Math.random()-0.5);
    // evitar que salga ya ordenada
    if (scrambled.join('')===current) scrambled.reverse();
    built = [];
    setText('paLevel', level); setText('paScore', score);
    render();
    // conectar botón de leer la palabra
    const readBtn = document.getElementById('paReadBtn');
    if (readBtn) {
      const fire = e => { e.preventDefault(); e.stopPropagation(); speak('La palabra es: ' + current); };
      readBtn.ontouchstart = fire;
      readBtn.onclick = fire;
    }
  }
  function render() {
    const lettersEl = document.getElementById('paLetters');
    const builtEl = document.getElementById('paBuilt');
    if(!lettersEl||!builtEl) return;
    lettersEl.innerHTML=''; builtEl.innerHTML='';
    scrambled.forEach((ch, i) => {
      const b = document.createElement('button');
      b.textContent = ch;
      b.disabled = ch === null;
      b.style.cssText = `width:46px;height:46px;border-radius:12px;border:none;font-family:'Fredoka One',cursive;font-size:1.4rem;cursor:pointer;background:${ch===null?'transparent':'linear-gradient(135deg,#2563EB,#06b6d4)'};color:#fff;-webkit-tap-highlight-color:transparent`;
      if (ch !== null) {
        const pick = e => { e.preventDefault(); e.stopPropagation(); addLetter(i); };
        b.addEventListener('touchstart', pick, {passive:false});
        b.addEventListener('click', pick);
      }
      lettersEl.appendChild(b);
    });
    built.forEach(ch => {
      const d = document.createElement('div');
      d.textContent = ch;
      d.style.cssText = "width:42px;height:48px;border-radius:10px;background:#FFD600;color:#2D1B4E;display:flex;align-items:center;justify-content:center;font-family:'Fredoka One',cursive;font-size:1.3rem";
      builtEl.appendChild(d);
    });
  }
  function addLetter(i) {
    if (scrambled[i] === null) return;
    built.push(scrambled[i]); scrambled[i] = null; SFX.click();
    render();
    if (built.length === current.length) {
      if (built.join('') === current) {
        score++; setText('paScore', score); SFX.point();
        awardBadge('first_win');
        if (score >= 5) awardBadge('word_smith');
        if (built.length>0) {
          // ¿subir de nivel? cada 3 palabras
          if (score % 3 === 0 && level < WORDS.length) {
            level++;
            showLevelScreen(level, pickWord, '¡Palabras más largas!');
          } else { celebrate(); setTimeout(pickWord, 800); }
        }
      } else {
        SFX.hit(); showToast('🤔 Casi... ¡intenta de nuevo!');
        setTimeout(pickWord, 900);
      }
    }
  }
  window.paSkip = pickWord;
  pickWord();
}

